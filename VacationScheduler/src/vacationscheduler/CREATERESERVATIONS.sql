create table "ROOT".RESERVATIONS
(
	OWNER_EMAIL VARCHAR(89) not null,
	LOCATION VARCHAR(250) not null,
	DATE_OF_RESERVATION DATE not null,
	NUMBER_OF_NIGHTS INTEGER not null,
	UNIT_SIZE VARCHAR(200) not null,
	CONFIRMATION_NUMBER INTEGER not null,
	POINTS_REQUIRED_FOR_RESERVATION INTEGER not null,
	WAS_DISCOUNTED BOOLEAN not null,
	WAS_UPGRADED BOOLEAN not null,
	IS_BUYER_LINED_UP BOOLEAN not null,
	GUEST_EMAIL VARCHAR(89),
	AMOUNT_PAID DOUBLE,
	PAYMENT_METHOD VARCHAR(100),
	TOTAL_RENTING_FOR DOUBLE,

        CONSTRAINT GEMAIL FOREIGN KEY (GUEST_EMAIL)
                    REFERENCES GUESTS(EMAIL),
        CONSTRAINT OEMAIL FOREIGN KEY (OWNER_EMAIL)
                    REFERENCES OWNERS(EMAIL)

        
)